lista = ["Maçã", "Banana", "Laranja", "Uva", "Pera"]

# imprimindo os elementos da lista
print("Imprime toda a lista:", lista)


# imprimindo o primeiro elemento da lista
print("Imprime o primeiro elemento da lista:", lista[0])

# imprimindo o segundo elemento da lista
print("Imprime o segundo elemento da lista:", lista[1])

# imprimindo o terceiro elemento da lista
print("Imprime o terceiro elemento da lista:", lista[2])


# imprimindo todos os elementos da lista na respectiva ordem, com for
for fruta in lista:
    print(fruta)