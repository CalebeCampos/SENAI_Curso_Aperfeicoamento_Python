# tupla utiliza parenteses
# lista utiliza colchetes

dias_semana = ("Domingo", "Segunda", "Terça", "Quarta", "Quinta", "Sexta", "Sabado")

# imprimindo o tipo da variavel
print(type(dias_semana))

print("\n")

# imprimindo os valores da tupla
for dia in dias_semana:
    print(dia)


# na tupla é possivel apenas pesquisar valores
# na tupla os valores são imutaveis, ou seja, não é possivel alterar os valores da tupla
# para pegar um valor de uma elemento da tupla pelo indice utiliza-se colchetes


